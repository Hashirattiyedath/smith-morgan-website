  // Intersection Observer API
  let AllLi = document.querySelectorAll('.left_border');
  console.log(AllLi);

  let liOption = {
    root : null,
    rootMargin : '0px',
    threshold : '0.5',  
  }             

  let liObserver = new IntersectionObserver(function(entries) {
    /*console.log(entries);*/
    entries.forEach(function(entry) {
      if(entry.isIntersecting === true) {
        document.querySelector('.left_border').classList.toggle('left_border_new');
      }
      else { /*or false*/
        document.querySelector('.left_border').classList.remove('left_border_new');
      }
    })
  },liOption);

  AllLi.forEach((myList)=> {
    liObserver.observe(myList);
  })

</script>


